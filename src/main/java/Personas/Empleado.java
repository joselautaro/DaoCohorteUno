package Personas;


public class Empleado {
    
    private int id;
    
    private String empleado;
    
    public Empleado (){}
    
    public int getId(){
        return this.id;
    }
    
    public void setId(int id){
        this.id = id;
    }
    
    public String getEmpleado(){
        return this.empleado;
    }
    
    public void setEmpleado(String empleado){
        this.empleado = empleado;
    }
}
